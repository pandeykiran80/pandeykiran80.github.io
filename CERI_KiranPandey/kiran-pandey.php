               <p>Master of Science, Tribhuvan University, Nepal, 2017</p><br>
               
               <h2>Research Interest</h2>
               
               <p><strong><a href="https://pandeykiran80.github.io/" target="_blank">Website</a></strong></p>
               
               <p>My research focuses on uncovering the intricate mechanisms behind fault failures and seismic activities, aiming to contribute 
                  to effective hazard mitigation strategies for a safer world.
                  <br>
                  Using a range of methodologies such as laboratory experiments, analytical and numerical models, and statistical analyses, 
                  I investigate fault behavior and earthquake generation. Through rigorous seismic velocity analysis and investigations of
                   fault repeaters, I gain valuable insights into fault rupture dynamics. I study earthquake precursors in the 
                   laboratory by monitoring velocity under different stress and pore pressure conditions. </p>
               
               <p>I integrate observations and methods from various disciplines such as rock mechanics, earthquake seismology, 
                  and hydrogeology. This interdisciplinary approach allows me to tackle important questions in my research. 
                  I aim to understand how fault structures evolve and influence earthquake behavior, identify earthquake precursors and 
                  triggers, explore volcanic system behavior and associated seismic signatures, and utilize machine learning techniques to 
                  enhance our understanding of seismic and volcanic phenomena. </p><br><br>
                             
               <p>Feel free to contact me for any inquiries, opportunities, or collaboration. Should you have any questions, 
                  please do not hesitate to reach out.</p>
               
